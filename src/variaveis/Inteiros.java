package variaveis;

public class Inteiros {
    public static void main(String[] args) {

        byte Tam8bits = 127;
        short Tam16bits = 32767;
        int Tam32bits = 2147483647;
        long Tam64bits = 9223372036854775807L;

        System.out.println("byte: " + Tam8bits);
        System.out.println("short: " + Tam16bits);
        System.out.println("int: " + Tam32bits);
        System.out.println("long: " + Tam64bits);

    }
}
